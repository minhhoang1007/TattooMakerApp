package com.burhanrashid52.photoeditor.ui.main;

public class ModelDesgin {
    String nameDes;
    Integer imgDes;

    public ModelDesgin(String nameDes, Integer imgDes) {
        this.nameDes = nameDes;
        this.imgDes = imgDes;
    }

    public String getNameDes() {
        return nameDes;
    }

    public void setNameDes(String nameDes) {
        this.nameDes = nameDes;
    }

    public Integer getImgDes() {
        return imgDes;
    }

    public void setImgDes(Integer imgDes) {
        this.imgDes = imgDes;
    }
}
