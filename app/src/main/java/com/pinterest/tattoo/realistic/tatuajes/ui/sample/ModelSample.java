package com.pinterest.tattoo.realistic.tatuajes.ui.sample;

public class ModelSample {
    String nameDes;
    String imgDes;

    public ModelSample(String nameDes, String imgDes) {
        this.nameDes = nameDes;
        this.imgDes = imgDes;
    }

    public String getNameDes() {
        return nameDes;
    }

    public void setNameDes(String nameDes) {
        this.nameDes = nameDes;
    }

    public String getImgDes() {
        return imgDes;
    }

    public void setImgDes(String imgDes) {
        this.imgDes = imgDes;
    }
}
